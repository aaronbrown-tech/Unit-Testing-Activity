let calculatorSum = (num1, num2) => {
    return num1 + num2;
}

let calculatorMultiply = (num1, num2) => {
    return num1 * num2;
}

let calculatorSubtract = (num1, num2) => {
    return num1 - num2;
}

let calculatorDivide = (num1, num2) => {
    return num1 / num2;
}

module.exports = { calculatorSum, calculatorMultiply, calculatorDivide , calculatorSubtract }